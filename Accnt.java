// Account class with constructor

public class Accnt
{
	private String name; // instance variable
	// initialize constructor with parameter name
	public Accnt(String name)
	{
		this.name = name;
	}
	// set name method
	public void setName(String name)
	{
		this.name = name;
	}
	
	//method to get name
	public String getName()
	{
	return name;
	}

}